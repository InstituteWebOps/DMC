    {
        // return $request->phNo;
        $request->validate([
            'name' => 'required',
            'age' => 'required|digits:2',
            'phNo' => 'required|digits:10'
        ]);

        $post = new Volunteer;
        $post->name = $request->input('name');
        $post->age = (int)$request->input('age');
        $post->phNo = (int)$request->input('phNo');
        $post->save();

        return view('vol.thanks');

    }

